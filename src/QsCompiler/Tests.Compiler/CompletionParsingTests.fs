module Microsoft.Quantum.QsCompiler.Testing.CompletionParsingTests

open System.Collections.Generic
open Xunit
open Microsoft.Quantum.QsCompiler.TextProcessing.CompletionParsing

let private testContext text expected =
    Assert.Equal<IEnumerable<IdentifierKind>>(expected, identifierKinds text)

[<Fact>]
let ``Callable declaration parser tests`` () =
    testContext "" [Keyword "function"; Keyword "operation"]
    testContext "f" [Keyword "function"; Keyword "operation"]
    testContext "fun" [Keyword "function"; Keyword "operation"]
    testContext "o" [Keyword "function"; Keyword "operation"]
    testContext "opera" [Keyword "function"; Keyword "operation"]

[<Fact>]
let ``Function declaration parser tests`` () =
    testContext "function" [Keyword "function"]
    testContext "function " [Declaration]
    testContext "function Foo" [Declaration]
    testContext "function Foo " [NoIdentifier]
    testContext "function Foo (" [Declaration]
    testContext "function Foo (x" [Declaration]
    testContext "function Foo (x :" [Type]
    testContext "function Foo (x : Int" [Type]
    testContext "function Foo (x : Int)" [NoIdentifier]
    testContext "function Foo (x : Int) :" [Type]
    testContext "function Foo (x : Int) : " [Type]
    testContext "function Foo (x : Int) : Unit" [Type]
    testContext "function Foo (x : Int) : Unit " [NoIdentifier]
    testContext "function Foo (x : Int) : (Int, MyT" [Type]
    testContext "function Foo (x : Int) : (Int," [Type]
    testContext "function Foo (x : Int) : (Int, MyT " [NoIdentifier]
    testContext "function Foo (x : (" [Type]
    testContext "function Foo (x : (Int" [Type]
    testContext "function Foo (x : (Int," [Type]
    testContext "function Foo (x : (Int, " [Type]
    testContext "function Foo (x : (Int, Int" [Type]
    testContext "function Foo (x : (Int, Int " [NoIdentifier]
    testContext "function Foo (x : (Int, Int), " [Declaration]
    testContext "function Foo (f : (Int -> " [Type]
    testContext "function Foo (f : (Int -> Int)) : Unit" [Type]
    testContext "function Foo (f : (Int -> Int)) : Unit " [NoIdentifier]
    testContext "function Foo (q : ((Int -> Int) -> " [Type]
    testContext "function Foo (q : ((Int -> Int) -> Int " [NoIdentifier]
    testContext "function Foo (q : ((Int -> Int) -> Int)) : Unit" [Type]
    testContext "function Foo (q : ((Int->Int)->Int)) : Unit" [Type]
    testContext "function Foo (q : ((Int -> Int) -> Int)) : Unit" [Type]
    testContext "function Foo<" [Declaration]
    testContext "function Foo<'" [Declaration]
    testContext "function Foo<> (q : 'T) : Unit" [Type]
    testContext "function Foo<'T> (q : 'T) : Unit" [Type]
    testContext "function Foo<'T> (q : ('T -> Int)) : Unit" [Type]
    testContext "function Foo<'T> (q : ('T->Int)) : Unit" [Type]
    testContext "function Foo<'T> (q : (MyType -> Int)) : Unit" [Type]

